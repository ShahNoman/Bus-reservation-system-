<h1 style="text-align:center;color:#FC590A; padding-top:50px;">About Blue Bird Kohat Transport Corporation </h1>

<p style="margin-left:30px; margin-right:30px; line-height:30px; color:#666666;"> In order to cater to the transport needs of the travelling public of the then state of pakistan, M Blue Bird Kohat Transport Corporation(BBKTC) was inaugurated with 120 buses on 12th AUGUST 2016.
The State Transport, which was being administered as a Department of the Govt. of Pakistan was subsequently converted into an independent Corporation under Section 3 of the Road Transport Corporation Act, in today. The assets and liabilities of BBKTC excepting those of BTS unit as on 01-08-2016 were passed on to the new Corporation, which was named as BBKTC. The assets and liabilities of the residual BBKTC i.e. of BTS Unit were subsequently passed on to the Corporation on 1st of October 2016. Thus, Corporation was ultimately established for the entire State of pakistan.
At the beginning, the passenger transport services were operated in 6 Divisions- 5 Divisions operating kohat services and 1 Division operating city services of kohat. It had 37 Depots, 2 Regional Work Shops and a Central Office at kohat. There were 15 permanent and 30 temporary bus stations with 35 wayside shelters and 104 pick-up shelters. The total number of employees deployed was 9705 and the staff ratio per schedule was 9.43. The total number of routes operated was 1065 with 1029 schedules and route length of 32,134 miles, average daily scheduled mileage being 127571. The total number of inter-state routes operated by the Corporation on a reciprocal basis with the neighbouring States were 40 ie., 29 Peshawer, 1 lahore, 7 islamabad, 2 karachi and 1 multan. The total number of vehicles held was 1518 with average vehicle utilisation of 123.8 miles. The average number of passengers carried per day was 4.35 lakh. The rate of breakdown was 1.88 and that of accident was 1.19. Earning per Mile (EPM) realized was 161.6 Ps., and Cost per Mile (CPM) was 127.2 Ps., resulting in net profit margin of 34.4 Ps/mile   </p>


